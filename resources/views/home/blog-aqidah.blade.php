                <h3>Pengertian Tauhid.</h3>
                <p>
                  Di dalam bahasa arab, tauhid adalah mashdar dari kata وَحَّدَ – يُوَحِّدُ – تَوْحِيْدًا yang berarti mengesakan. Adapun menurut istilah, tauhid adalah “meyakini akan ke-esa-an Allah -subhanahu wa ta’ala- dalam rububiyah (penciptaan, pemeliharaan, pemilikan), uluhiyyah (ikhlas beribadah kepadaNya) dan dalam Al-Asmaa wash-shifaat (nama-nama dan sifat)-Nya“. Dan tauhid apabila dimutlakkan, maka maknanya adalah memurnikan seluruh peribadatan hanya untuk Allah ta’ala.
                </p>

                <blockquote>
                  <p>
                    Seorang muslim wajib mengimani akan keesaaan Allah ta’ala dan bahwasannya tidak ada tuhan yang berhak disembah melainkan Allah ta’ala, adapun kalimat Tauhid itu sendiri maka yang dimaksud ialah La ilaha illah yang berarti tidak ada yang berhak disembah selain Allah, di dalam al-Quran Allah ta’ala berfirman : “Dan tuhan kamu adalah tuhan yang Maha Esa, tidak ada tuhan selai Dia, yang Maha pengasih, Maha penyayang”. (QS. Al-Baqarah: 163)
                  </p>
                </blockquote>

                <p>
                  Dari pengertian Tauhid menurut istilah yang telah kita ketahui bersama, maka kita telah mengetahui bahwa Tauhid terbagi menjadi tiga, yaitu :
                  1. Tauhid Rububiyyah
                  2. Tauhid Uluhiyyah
                  3. Tauhid Asma’ wa Shifat
                </p>

                <h3> Pertama : Tauhid Rububiyah.</h3>
                <p>
                  Artinya mengesakan Allah –subhanahu wa ta’ala- dalam hal perbuatanNya. Seperti mencipta, memberi rezeki, menghidupkan dan mematikan, mendatangkan bahaya, memberi manfaat, dan lain-lain yang merupakan perbuatan-perbuatan khusus bagi Allah –subhanahu wa ta’ala- yang tidak bisa dilakukan oleh manusia maupun jin dan seluruh Alam semesta yang telah Dia ciptakan. Seorang muslim wajib meyakini bahwa Allah –subhanahu wa ta’ala– tidak memiliki sekutu dalam RububiyahNya.
                </p>
                <p>
                  Mengenai Tauhid Rububiyah ini kaum Musyrikin di zaman Nabi –salallahu ‘alaihi wa sallam- pun meyakininya, Allah Ta’ala telah berfirman di dalam Al-Qur’an :

                  “Katakanlah, “Siapakah Tuhan yang memiliki langit yang tujuh dan yang memiliki ‘Arsy yang agung ?mereka akan menjawab milik Allah, “ katakanlah, “maka kenapa kamu tidak bertakwa ?”

                  Masih banyak lagi ayat–ayat yang menunjukkan bahwa kaum musyrikin mengikrarkan Tauhid Rububiyah, itu dikarenakan Tauhid Rububiyah merupakan fitroh setiap insan yang telah diciptakan di dunia ini.

                  Rasulullah Salallahu Alaihi wa Sallam telah bersabda :

                  “Setiap anak yang dilahirkan di dalam keadaan Fitroh maka kedua orang tuanyalah yang menjadikannya yahudi, nashrani, atau majusi”. (HR.Bukhari dan Muslim)

                </p>
                <img src="assets/img/blog/aqidah-2.jpeg" class="img-fluid w-100" alt="">

                <h3> Kedua : Tauhid Uluhiyah.</h3>
                <p>
                  Artinya, mengesakan Allah –subhanahu wa ta’ala- dalam beberapa macam peribadatan yang telah disyariatkan olehNya. Seperti, shalat, puasa, zakat, haji, do’a, nadzar, sembelihan, berharap, cemas, takut, dan sebagainya yang termasuk jenis-jenis ibadah.

                  Mengesakan Allah –subhanahu wa ta’ala- dalam hal-hal tersebut dinamakan Tauhid Uluhiyah (disebut pula dengan tauhid ibadah, -ed); dan Tauhid jenis inilah yang dituntut oleh Allah –subhanahu wa ta’ala- dari hamba-hambaNya, yaitu mengesakan Allah dalam hal ibadah.
                </p>
                <p>
                  Jika mereka mengikrarkan Tauhid Rububiyah, maka hendaknya juga mengakui Tauhid Uluhiyah. Para Rasul diutus oleh Allah adalah untuk menyeru kepada Umat mereka agar meyakini Tauhid Uluhiyah meyakini dalam artian melaksanakan apa yang telah Allah perintahkan kepada hamba–hambaNya dalam bentuk peribadahan dan tidak beribadah untuk selainNya. Allah –subhanahu wa ta’la– telah berfirman di dalam Al-Qur’an :

                َ وَلَقَدْ بَعَثْنَا فِي كُلِّ أُمَّةٍ رَسُولًا أَنِ اُعْبُدُوا اللَّهَ وَاجْتَنِبُوا الطَّاغُوتَ

                “Dan sungguhnya Kami telah mengutus rasul pada tiap-tiap umat (untuk menyerukan): “Sembahlah Allah (saja), dan jauhilah Thaghut…”. (QS. An-Nahl: 36)
                </p>

                <h3>Ketiga : Tauhid Asma was Sifat.</h3>
                <p>
                  Yaitu menetapkan nama-nama dan sifat-sifat untuk Allah –subhanahu wa ta’ala- sesuai dengan yang telah ditetapkan oleh Allah untuk diriNya maupun yang telah ditetapkan oleh Rasulullah –shallallahu ‘alaihi wa sallam- tanpa mentakwil (ta’wil), memisalkan (tamtsil), menanyakan bagaimananya (takyif) dan meniadakan (ta’thil) dari nama dan sifat tsb. Hali ini pula harus disertai dengan meniadakan kekurangan-kekurangan dan aib-aib yang ditiadakan oleh Allah terhadap diriNya, dan apa yang ditiadakan oleh Rasulullah –shallallahu ‘alaihiwa sallam- bagi Allah ta’ala, karena Allah ta’alasesungguhnya maha sempurna dan sangat jauh dari aib ataupun kekurangan.
                </p>

                <p>
                  Allah Ta’ala telah menyatakan bahwa Ia memiliki nama-nama yang husna (baca: sangat baik/indah) dan Ia memerintahkan kita untuk berdo’a dengan nama–namaNya, Allah telah berfirman di dalam Al-Qur’an :

                  وَلِلَّهِ الأسماء الحسنى فادعوه بها و ذَرُوا الَّذِينَ يُلْحِدُونَ فِي أَسْمَائِهِ سَيُجْزَوْنَ مَا كَانُوا يَعْمَلُونَ

                  “Hanya milik Allah asmaa-ul husna, Maka berdoalah kepada-Nya dengan menyebut asmaa-ul husna itu dan tinggalkanlah orang-orang yang menyimpang dari kebenaran dalam nama-namaNya nanti mereka akan mendapat balasan terhadap apa yang telah mereka kerjakan” (Al-A’raf : 180).

                  Dan firmanNya :

                  لَيْسَ كَمِثْلِهِ شَيْءٌ وَهُوَ السَّمِيعُ البَصِيرُ

                  “Tidak ada sesuatupun yang serupa dengan Dia, dan Dia-lah yang Maha mendengar lagi Maha melihat.” (QS. As-Syura’ : 11)
                </p>

                <p>
                  Tiga jenis Tauhid di atas, wajib diketahui oleh setiap muslim (dan segala ubudiyah kita kepada Allah wajib dengan ketiga tauhid itu semua) karena Tauhid adalah pondasi keimanan seseorang kepada Allah ta’ala, sehingga hendaklah kita senantiasa menjaga kemurnian tauhid kita di dalam beribadah kepada Allah ta’ala dari apa saja yang dapat merusak Tauhid kita. Wallahu a’lam bis-showab.
                </p>

